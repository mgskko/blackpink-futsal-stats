// ===== HELPER FUNCTIONS =====
export function getPlayerName(id: number): string {
  return players.find(p => p.id === id)?.name ?? `선수#${id}`;
}

export function getVenueName(id: number): string {
  return venues.find(v => v.id === id)?.name ?? "미정";
}

export function getMatchResult(matchId: number): { ourTeam: Team; opponentTeam: Team; ourResult: Result; opponentResult: Result } | null {
  const matchTeams = teams.filter(t => t.matchId === matchId);
  const ourTeam = matchTeams.find(t => t.isOurs && t.name === "버니즈") || matchTeams.find(t => t.isOurs);
  const opponentTeam = matchTeams.find(t => !t.isOurs) || matchTeams.find(t => t.id !== ourTeam?.id);
  if (!ourTeam || !opponentTeam) return null;

  const ourResult = results.find(r => r.teamId === ourTeam.id && r.matchId === matchId);
  const opponentResult = results.find(r => r.teamId === opponentTeam.id && r.matchId === matchId);
  if (!ourResult || !opponentResult) return null;

  return { ourTeam, opponentTeam, ourResult, opponentResult };
}

